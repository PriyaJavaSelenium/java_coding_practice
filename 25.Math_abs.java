package javaPracticeDaily_w3School;

public class Math_abs {
    public static void main(String[] args) {
        System.out.println(Math.abs(-8)); // when we give negative number it gives positive
        System.out.println(Math.abs(-4.7)); // when we give negative float number it gives positive float
        System.out.println(Math.abs(5)); // when we give positive int it gives positive int
    }
}
